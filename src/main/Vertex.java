package main;

public class Vertex {
	private double x;
	private double y;
	private double z;

	public Vertex(double x, double y, double z){
		this.x = x;
		this.y = y;
		this.z = z;
	}

	public void setX(double x){
		this.x = x;
	}

	public double getX(){
		return this.x;
	}

	public void setY(double y){
		this.y = y;
	}

	public double getY(){
		return this.x;
	}

	public void setZ(double z){
		this.z = z;
	}

	public double getZ(){
		return this.x;
	}


}
